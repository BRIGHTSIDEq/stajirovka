cells.append(md("""### Q4. Логистическая регрессия: формула, обучение, проверка коэффициентов
`[СберБанк, Яндекс Карты, VK, МТС, Young&&Yandex]`

**Ответ:**

**Формула:**

$$p(y=1 | x) = \\sigma(w^T x + b) = \\frac{1}{1 + e^{-(w^T x + b)}}$$

где σ — сигмоида. **Логит** — это $\\log\\frac{p}{1-p} = w^T x + b$ (линейная функция от признаков).

Знаменатель $1 + e^{-z}$: при z → +∞ → p → 1, при z → -∞ → p → 0.

**Функция потерь** — бинарная кросс-энтропия:
$$L = -\\frac{1}{N}\\sum_i [y_i \\log(p_i) + (1-y_i) \\log(1-p_i)]$$

**Обучение:** градиентный спуск. Аналитического решения нет (в отличие от линейной регрессии), т.к. функция потерь не квадратична.

**Шаг градиентного спуска:**
$$w := w - \\alpha \\cdot \\frac{\\partial L}{\\partial w}$$

**Проверка значимости коэффициентов:** z-тест (коэффициент / стандартная ошибка). p-value < 0.05 → значим.

**WOE (Weight of Evidence):** монотонное преобразование категориальных признаков. WOE_i = ln(Distribution_Events_i / Distribution_Non-Events_i). Позволяет корректно использовать нелинейные зависимости в линейной модели.
"""))

