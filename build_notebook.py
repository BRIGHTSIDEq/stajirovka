cells.append(md("""### Q7. Регуляризация L1 vs L2: в чём разница, почему L1 зануляет веса?
`[Авито, Магнит, Lamoda, СберБанк, ДомРФ]`

**Ответ:**

**L2 (Ridge):** к функции потерь добавляется $\\lambda \\sum w_i^2$. Градиент: $\\frac{\\partial}{\\partial w} \\lambda w^2 = 2\\lambda w$. При каждом шаге вес уменьшается пропорционально своему значению — **"weight decay"**. Веса стремятся к нулю, но никогда точно не обнуляются.

**L1 (Lasso):** добавляется $\\lambda \\sum |w_i|$. Градиент: $\\lambda \\cdot \\text{sign}(w_i)$. **Постоянный шаг** в сторону нуля, не зависящий от величины веса. Поэтому L1 **обнуляет** малые веса полностью → **автоматический feature selection**.

**Геометрическая интерпретация:** область допустимых значений L2 — сфера (нет угловых точек), L1 — ромб с угловыми точками на осях координат. Решение стремится к угловой точке → некоторые координаты = 0.

**Elastic Net:** комбинация L1 + L2: $\\lambda_1 \\sum |w_i| + \\lambda_2 \\sum w_i^2$. Используется при мультиколлинеарности.

**Влияние на Bias-Variance:** регуляризация увеличивает bias, уменьшает variance. При λ = 10^6 → модель вырождается в константу (все веса → 0).
"""))

