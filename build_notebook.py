cells.append(md("""### Q5. Случайный лес vs Градиентный бустинг: отличия, принципы работы
`[Без компании, VK, Lamoda, ОКБ, Сибур, Мегафон, Conundrum, Банк СТО, Райффайзен]`

**Ответ:**

**Случайный лес (Random Forest)** — **Bagging** + случайный выбор признаков:
1. Строим N деревьев, каждое — на bootstrap-выборке (случайная выборка с возвращением).
2. На каждом разбиении рассматриваем случайное подмножество признаков (√p для классификации, p/3 для регрессии).
3. Результат — усреднение (регрессия) или голосование (классификация).
4. **Снижает variance** (деревья независимы и некоррелированы). Bias почти не меняется.

**Градиентный бустинг** — **последовательный** ансамбль:
1. Инициализируем предсказание константой (среднее y для регрессии, log(p/(1-p)) для классификации).
2. Вычисляем **псевдо-остатки** (отрицательный градиент функции потерь).
3. Обучаем новое дерево предсказывать эти остатки.
4. Добавляем с шагом (learning rate): $F_m(x) = F_{m-1}(x) + \\eta \\cdot h_m(x)$.
5. Повторяем M раз.
6. **Снижает bias**. При большом M — риск переобучения.

**Почему не бустинг на линейных моделях?** Остатки линейной модели — тоже линейная функция от признаков, следующая линейная модель предскажет то же самое. Деревья могут аппроксимировать любые нелинейные зависимости.

| | Случайный лес | Градиентный бустинг |
|---|---|---|
| Тип ансамбля | Параллельный (Bagging) | Последовательный |
| Что снижает | Variance | Bias |
| Интерпретация | Проще | Сложнее |
| Скорость обучения | Быстрее | Медленнее |
| Устойчивость к выбросам | Выше | Ниже |
| Удаление 1 модели | Малый эффект | Малый эффект, если моделей много |
"""))

