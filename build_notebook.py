cells.append(md("""### Q8. Что такое ROC-AUC? Подробный разбор для бизнеса и технических вопросов
`[Ozon, Lamoda, Самокат, Авито, Сибур, МТС, Conundrum, Райффайзен, Young&&Yandex]`

**Ответ:**

**ROC-кривая** — кривая в пространстве (FPR, TPR) при изменении порога классификации от 1 до 0.
- **TPR (True Positive Rate) = Recall** = TP / (TP + FN). По оси Y.
- **FPR (False Positive Rate)** = FP / (FP + TN). По оси X.
- Каждый шаг вверх — объект с реальным классом 1 (шаг = 1/P).
- Каждый шаг вправо — объект с реальным классом 0 (шаг = 1/N).

**AUC** = площадь под ROC-кривой. Вероятность того, что случайный позитивный объект получит скор выше случайного негативного.

**Как объяснить бизнесу:** если у нас 100 клиентов: 50 хороших, 50 плохих — с какой вероятностью модель даст хорошему клиенту более высокий скор, чем плохому? AUC = 0.9 → с вероятностью 90%.

**Ответы на частые вопросы:**
- **Возвести скор в квадрат** → порядок ранжирования сохраняется (если скоры в [0,1]) → AUC не изменится.
- **Умножить скоры на -1** → порядок инвертируется → AUC = 1 - AUC_old.
- **Умножить на константу > 0** → порядок не меняется → AUC не изменится.
- **AUC = 0.18** → инвертировать предсказания (multiply by -1) → AUC = 0.82. Модель работает лучше случайного, просто с обратным знаком.
- **ROC-AUC при дублировании** → не изменится (порядок ранжирования не нарушается).
- **Связь с Precision/Recall:** ROC-AUC не учитывает дисбаланс, PR-AUC — учитывает. При сильном дисбалансе лучше PR-AUC.
"""))

